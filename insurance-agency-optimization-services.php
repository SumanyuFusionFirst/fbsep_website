        ];
        let currentTextIndex = 0;
        let currentText = texts[currentTextIndex];
        let textArray = [];
        let loopTimer;

        function typeText() {
          if (textArray.length < currentText.length) {
            document.getElementById("type_text").textContent +=
              currentText[textArray.length];
            textArray.push(currentText[textArray.length]);
          } else {
            eraseText();
            return;
          }
          loopTimer = setTimeout(typeText, 170);
        }

        function eraseText() {
          if (textArray.length > 0) {
            textArray.pop();
            document.getElementById("type_text").textContent =
              currentText.substring(0, textArray.length);
          } else {
            changeText();
            return;
          }
          loopTimer = setTimeout(eraseText, 170);
        }

        function changeText() {
          currentTextIndex = (currentTextIndex + 1) % texts.length;
          currentText = texts[currentTextIndex];
          textArray = [];
          typeText();
        }

        typeText();
      });
